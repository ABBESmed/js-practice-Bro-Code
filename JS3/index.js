// operands <=> values , variables .....
// operations <=> + - / * %

let students = 10;
// new student join the class so we add +1

students = students + 1; // this is add operator +
students = students - 1; // this is sub operator -
students = students * 2; // this is mul operator *
students = students / 2; // this is div operator /
students = students ** 3; // this is exp operator **
students = students % 100; // this is modulo operator %
console.log(students);

//augmented assignment operators
let numberOfIphones = 20;
numberOfIphones += 1;
console.log(numberOfIphones);
numberOfIphones -= 1;
console.log(numberOfIphones);
numberOfIphones *= 2;
console.log(numberOfIphones);
numberOfIphones /= 2;
console.log(numberOfIphones);
numberOfIphones **= 3;
console.log(numberOfIphones);
numberOfIphones %= 100;
console.log(numberOfIphones);

// increment decrement

let ram = 1;
ram++; // increment
console.log(ram);

ram--; // decrement
